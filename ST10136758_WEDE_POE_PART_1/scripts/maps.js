document.addEventListener(
"DOMContentLoaded",
function(){


const mapElement =
document.getElementById(
"spca-map"
);



if(!mapElement){
    return;
}





const map =
L.map(
"spca-map"
)
.setView(
[-29.0,24.0],
5
);





L.tileLayer(

"https://tile.openstreetmap.org/{z}/{x}/{y}.png",

{

attribution:
"&copy; OpenStreetMap contributors"

}

).addTo(map);






let markers = [];




// Display markers

function displayLocations(locations){



// Remove old markers

markers.forEach(function(marker){

map.removeLayer(marker);

});


markers = [];





locations.forEach(function(spca){



let marker =
L.marker(
spca.location
)
.addTo(map);



marker.bindPopup(`


<h3>
${spca.name}
</h3>


<p>
<strong>
Province:
</strong>
${spca.province}
</p>


<p>
${spca.address}
</p>


<p>
📞 ${spca.phone || "Contact SPCA directly"}
</p>


<p>
${spca.description}
</p>


<a href="https://www.google.com/maps/search/?api=1&query=${spca.location[0]},${spca.location[1]}"
target="_blank">

Get Directions

</a>


`);




markers.push(marker);


});





// Adjust zoom

if(locations.length > 0){



const group =
L.featureGroup(
markers
);



map.fitBounds(
group.getBounds(),
{
padding:[50,50]
}
);



}


}







// Load all locations initially

displayLocations(
spcaLocations
);







// Province search

document
.getElementById(
"search-province"
)
.addEventListener(
"click",
function(){



const selectedProvince =
document.getElementById(
"province-search"
)
.value;





if(selectedProvince === "all"){


displayLocations(
spcaLocations
);


}


else {



const filteredLocations =
spcaLocations.filter(
function(spca){


return spca.province === selectedProvince;


}

);



displayLocations(
filteredLocations
);



}



});



});